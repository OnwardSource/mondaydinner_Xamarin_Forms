    /// <summary>
    /// An empty page that can be used on its own or navigated to within a Frame.
    /// </summary>
    public sealed partial class MainPage : IAuthenticate
    {
        // Define a authenticated user.
        private MobileServiceUser user;

        public async Task<bool> Authenticate()
        {
            string message = string.Empty;
            var success = false;

            try
            {
                // Sign in with Facebook login using a server-managed flow.
                if (user == null)
                {
                    user = await YodelManager.DefaultManager.CurrentClient
                        .LoginAsync(MobileServiceAuthenticationProvider.Facebook);
                    if (user != null)
                    {
                        success = true;
                        message = string.Format("You are now signed-in as {0}.", user.UserId);
                    }
                }

            }
            catch (Exception ex)
            {
                message = string.Format("Authentication Failed: {0}", ex.Message);
            }

            // Display the success or failure message.
            await new MessageDialog(message, "Sign-in result").ShowAsync();

            return success;
        }

        public MainPage()
        {
            this.InitializeComponent();

            // Initialize the authenticator before loading the app.
            mondaydinner.App.Init(this);

            Xamarin.FormsMaps.Init("UwZn0iDHnk71y6TH4Uai~v0IsdE0rkwsDOtAr2dWRGQ~AnoWouc9IRkCKm3BXcIZ17FQnA71lzcU9_eAboAxeHITPaae8FiR-5fhv0TA_W4Q");

            LoadApplication(new mondaydinner.App());
        }
    }
